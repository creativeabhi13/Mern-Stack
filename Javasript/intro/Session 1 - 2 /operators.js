// Arithmetoc operators

// +,-,*,/,%,++,--,**
// + => addition
// - => subtraction
// * => multiplication
// / => division
// % => modulus
// ++ => increment
// -- => decrement
// ** => exponentiation
let a=10;
let b=20;
let sum=a+b;
let subt=b-a;
let mul=a*b;
let div=b/a;
let mod=b%a;
let inc=++a;
let dec=--b;
let exp=a**2;
console.log(sum,subt,mul,div,mod,inc,dec,exp);

let x=-10;
let y=-3;
let mod1=x%y;
console.log(mod1);


// Relational operators
// <,>,<=,>=,==,!=  
// < => less than
// > => greater than
//<= => less than or equal to`
// >= => greater than or equal to
// == => equal to
// != => not equal to
// === => strict equal to 
let p=10;   
let q=20;
let less=p<q;
let greater=p>q;
let lesseq=p<=q;
let greateq=p>=q;
let equal=p==q;
let noteq=p!=q;
let stricteq=p===q;
console.log(less,greater,lesseq,greateq,equal,noteq,stricteq);

// Logical operators    
// &&,||,!
// && => and
// || => or
// ! => not
let m=10;
let n=20;

let and=m>5 && n<30;
let or=m>5 || n<30;
let not=!(m>5);
console.log(and,or,not);

