// Arithmetoc operators

// +,-,*,/,%,++,--,**
// + => addition
// - => subtraction
// * => multiplication
// / => division
// % => modulus
// ++ => increment
// -- => decrement
// ** => exponentiation
let a=10;
let b=20;
let sum=a+b;
let subt=b-a;
let mul=a*b;
let div=b/a;
let mod=b%a;
let inc=++a;
let dec=--b;
let exp=a**2;
console.log(sum,subt,mul,div,mod,inc,dec,exp);

